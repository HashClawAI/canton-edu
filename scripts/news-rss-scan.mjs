#!/usr/bin/env node
/**
 * Fetches Canton-related RSS feeds, compares item links to URLs already in
 * en/zh news.items in translations.ts, writes news-candidates.md for CI or local triage.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const TRANSLATIONS = path.join(ROOT, 'src', 'i18n', 'translations.ts');
const OUT = path.join(ROOT, 'news-candidates.md');

const RSS_URLS = [
  'https://news.google.com/rss/search?q=Canton+Network&hl=en&gl=US&ceid=US:en',
  'https://news.google.com/rss/search?q=Canton+Coin&hl=en&gl=US&ceid=US:en',
];

const UA =
  'Mozilla/5.0 (compatible; canton-edu-news-scan/1.0; +https://github.com/HashClawAI/canton-edu)';

function extractTag(block, tag) {
  const re = new RegExp(`<${tag}[^>]*>([\\s\\S]*?)</${tag}>`, 'i');
  const m = block.match(re);
  if (!m) return '';
  return decodeBasicEntities(
    m[1]
      .replace(/<!\[CDATA\[([\s\S]*?)\]\]>/gi, '$1')
      .replace(/<[^>]+>/g, '')
      .trim(),
  );
}

function decodeBasicEntities(s) {
  return s
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'");
}

function parseRssItems(xml) {
  const items = [];
  const itemRe = /<item>([\s\S]*?)<\/item>/gi;
  let m;
  while ((m = itemRe.exec(xml))) {
    const block = m[1];
    const title = extractTag(block, 'title');
    let link = extractTag(block, 'link');
    const pubDate = extractTag(block, 'pubDate');
    if (!title || !link) continue;
    link = link.trim();
    items.push({ title, link, pubDate });
  }
  return items;
}

function extractNewsItemsBlocks(source) {
  const zhIdx = source.indexOf('\n  zh:');
  const enPart = zhIdx === -1 ? source : source.slice(0, zhIdx);
  const zhPart = zhIdx === -1 ? '' : source.slice(zhIdx);
  const re = /news:\s*\{[\s\S]*?items:\s*\[([\s\S]*?)\]\s*,\s*cipWatchTitle/s;
  const enM = enPart.match(re);
  const zhM = zhPart.match(re);
  return [enM?.[1] ?? '', zhM?.[1] ?? ''];
}

function urlsInNewsBlocks(blocks) {
  const set = new Set();
  const urlRe = /url:\s*'([^']+)'/g;
  for (const block of blocks) {
    let m;
    while ((m = urlRe.exec(block))) {
      const u = m[1].trim();
      if (u.startsWith('http')) set.add(normalizeUrl(u));
    }
  }
  return set;
}

/** Strip common tracking params for dedupe */
function normalizeUrl(u) {
  try {
    const x = new URL(u);
    if (x.hostname === 'news.google.com' && x.pathname.startsWith('/rss/articles/')) {
      return u;
    }
    return `${x.origin}${x.pathname}`;
  } catch {
    return u;
  }
}

async function fetchText(url) {
  const res = await fetch(url, { headers: { 'User-Agent': UA } });
  if (!res.ok) throw new Error(`HTTP ${res.status} ${url}`);
  return res.text();
}

function mergeDedupe(allItems) {
  const byLink = new Map();
  for (const it of allItems) {
    const key = normalizeUrl(it.link);
    if (!byLink.has(key)) byLink.set(key, { ...it, link: it.link });
  }
  return [...byLink.values()];
}

async function main() {
  const source = fs.readFileSync(TRANSLATIONS, 'utf8');
  const blocks = extractNewsItemsBlocks(source);
  const known = urlsInNewsBlocks(blocks);

  const raw = await Promise.all(RSS_URLS.map(fetchText));
  const merged = mergeDedupe(raw.flatMap(parseRssItems));

  const unknown = merged.filter((it) => !known.has(normalizeUrl(it.link)));

  const lines = [
    '## Canton news — RSS candidates (not yet in translations.ts)',
    '',
    '_Generated by `scripts/news-rss-scan.mjs`. Triager: add bilingual rows to `en.news.items` / `zh.news.items`, then close this issue._',
    '',
  ];

  for (const it of unknown.slice(0, 25)) {
    lines.push(`- **${it.title}**`);
    lines.push(`  - Link: ${it.link}`);
    if (it.pubDate) lines.push(`  - Published: ${it.pubDate}`);
    lines.push('');
  }

  if (unknown.length === 0) {
    lines.push('_No new links vs current `news.items` URLs._');
  } else if (unknown.length > 25) {
    lines.push(`_…and ${unknown.length - 25} more (trimmed)._`);
  }

  const body = lines.join('\n');
  fs.writeFileSync(OUT, body, 'utf8');
  process.stdout.write(body + '\n');

  const has = unknown.length > 0;
  const ghOut = process.env.GITHUB_OUTPUT;
  if (ghOut) {
    fs.appendFileSync(ghOut, `has_candidates=${has}\n`);
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
